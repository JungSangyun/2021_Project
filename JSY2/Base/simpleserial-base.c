    SEED_F(L, R, RK[12]);
    SEED_F(R, L, RK[13]);
    SEED_F(L, R, RK[14]);
    SEED_F(R, L, RK[15]);
    
    
	
    
	trigger_low();
    
    pt[0] = R[0] >>24 &0xff;
    pt[1] = R[0] >>16 &0xff;
    pt[2] = R[0] >>8 &0xff;
    pt[3] = R[0]  &0xff;
    pt[4] = R[1] >>24 &0xff;
    pt[5] = R[1] >>16 &0xff;
    pt[6] = R[1] >>8 &0xff;
    pt[7] = R[1]  &0xff;
