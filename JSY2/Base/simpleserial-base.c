const unsigned long SEED_KC[16] = {
	0x9e3779b9 , 0x3c6ef373,
	0x78dde6e6 , 0xf1bbcdcc,
	0xe3779b99 , 0xc6ef3733,
	0x8dde6e67 , 0x1bbcdccf,
	0x3779b99e , 0x6ef3733c,
	0xdde6e678 , 0xbbcdccf1,
	0x779b99e3 , 0xef3733c6,
	0xde6e678d , 0xbcdccf1b
};
void SEED_G(unsigned long *s) { // s는 그전에 넘어온것 s = 32bit  x0 = 8bit
    unsigned char m[4];
    m[0] = 0xfc;
    m[1] = 0xf3;
    m[2] = 0xcf;
    m[3] = 0x3f;
	unsigned long z[4];
	unsigned char y[4];
	unsigned long temp = 0;

	y[0] = SEED_S1box[((*s) >> 0) & 0xff];
	y[1] = SEED_S2box[((*s) >> 8) & 0xff];
	y[2] = SEED_S1box[((*s) >> 16) & 0xff];
	y[3] = SEED_S2box[((*s) >> 24) & 0xff];

	z[3] = (y[0] & m[3]) ^ (y[1] & m[0]) ^ (y[2] & m[1]) ^ (y[3] & m[2]); // 0x3f 0xfc 0xf3 0xcf
	z[2] = (y[0] & m[2]) ^ (y[1] & m[3]) ^ (y[2] & m[0]) ^ (y[3] & m[1]); // 0xcf 0x3f 0xfc 0xf3
	z[1] = (y[0] & m[1]) ^ (y[1] & m[2]) ^ (y[2] & m[3]) ^ (y[3] & m[0]); // 0xf3 0xcf 0x3f 0xfc 
	z[0] = (y[0] & m[0]) ^ (y[1] & m[1]) ^ (y[2] & m[2]) ^ (y[3] & m[3]); // 0xfc 0xf3 0xcf 0x3f

	temp ^= z[0];
	temp ^= (z[1] << 8);
	temp ^= (z[2] << 16);
	temp ^= (z[3] << 24);
	*s = temp;
}

void KeyGen(unsigned long A , unsigned long B , unsigned long C, unsigned long D ,unsigned long RK[][2]){
    
    int i;
    unsigned char tmp1;
    unsigned char tmp2;
    unsigned long T0;
    unsigned long T1;
    for(i = 0 ; i < 16 ; i ++)
    {
        T0 = A + C - SEED_KC[i];
        T1 = C - D + SEED_KC[i];
        
        SEED_G(&T0);
        SEED_G(&T1);
        RK[i][0] = T0;
        RK[i][1] = T1;
        if(i % 2 == 0){
            tmp1 = A & 0xff;
            tmp2 = B & 0xff;
            A = (A >> 8) ^ (tmp2 << 24);
            B = (B >> 8) ^ (tmp1 << 24);
       
        }
        else{
            tmp1 = (C >>24) & 0xff;
            tmp2 = (D >> 24) & 0xff;
            C = (C << 8) ^ tmp2;
            D = (D << 8) ^ tmp1;
        }
    
    }
}
    
void SEED_F(unsigned long L[2] , unsigned long R[2] , unsigned long k[2]){
    unsigned long temp[2]; // 16비트
	temp[0] = R[0] ^ k[0];
	temp[1] = R[1] ^ k[1];

	temp[1] ^= temp[0];

	SEED_G(temp + 1); // temp[1]의 pointer를 전달
	temp[0] += temp[1];

	SEED_G(temp);
	temp[1] += temp[0];

	SEED_G(temp + 1);
	temp[0] += temp[1];


	L[0] ^= temp[0]; // temp[0] = R[0] 
	L[1] ^= temp[1];
        
}
    
